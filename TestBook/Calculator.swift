import Foundation

class Calculator {
    
    func add(_ x: Int, _ y: Int) -> Int {
        return x + y
    }
    
    func subtract(_ x: Int, _ y: Int) -> Int {
        return x - y
    }
    
    func multiple(_ x: Int, _ y: Int) -> Int {
        return x * y
    }
    
    func division(_ x: Int, _ y: Int) -> Int {
        return x / y
    }
    
}
